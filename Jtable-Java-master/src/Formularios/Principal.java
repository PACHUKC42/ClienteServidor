    // funcion para rellenar la tabla
    private void mostrar() {        
        DefaultTableModel modelo = new DefaultTableModel();               
        ResultSet rs = Database.getTabla("select nombres,apellidos from usuarios");
        modelo.setColumnIdentifiers(new Object[]{"Nombres", "Apellidos"});
        try {
            while (rs.next()) {
                // a√±ade los resultado a al modelo de tabla
                modelo.addRow(new Object[]{rs.getString("nombres"), rs.getString("apellidos")});
            }            
            // asigna el modelo a la tabla
            table.setModel(modelo);            
        } catch (Exception e) {
            System.out.println(e);
        }

    }


