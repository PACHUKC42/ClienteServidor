    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        camposC();
        if(this.verCampo){
            Conexion conx = new Conexion();
            conx.conectar();
            int id=Integer.parseInt((String) lista.getSelectedItem());
            int piezas= Integer.parseInt(pie.getText());
            int cliente1= Integer.parseInt(cliente.getText());
            Equipo actualizacion = new Equipo(id, cliente1, desc.getText(), mar.getText(), mod.getText(), piezas);
            conx.modificaEquipo(actualizacion);
            JOptionPane.showMessageDialog(null, "Datos insertados correctamente");
            verCampo=false;
            limpiar();
            conx.desconectar();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

