        public void camposC() {
        if (!"".equals(this.desc.getText()) && !"".equals(this.mar.getText()) && !"".equals(this.mod.getText()) && !"".equals(this.pie.getText())&& !"".equals(this.cliente.getText())) {
            this.verCampo = true;
        } else {
            this.verCampo = false;
            JOptionPane.showMessageDialog(null, "No puede haber campos vacios");

        }
    }
    
    public void limpiar(){
        this.cliente.setText(null);
        this.desc.setText(null);
        this.mar.setText(null);
        this.mod.setText(null);
        this.pie.setText(null);

    }
